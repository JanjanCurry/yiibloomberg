    <div class="panel-group" id="accordion">
        <p>The Complete Intelligence global model contains billions of data items across economics, trade, currencies and commodities. We have intentionally designed an integrated global model to ensure that actions in one market, country or sector of the economy are reflected elsewhere in markets, industries and the global economy.</p>

        <p>For each forecast line, we test more than 1 million functions from nearly 100 industry sectors and over 100 countries against the historical data series. These functions include the following data:</p>

        <ul>
            <li>International Trade</li>
            <li>Economic Indicators</li>
            <li>Currencies</li>
            <li>Commodity Prices</li>
            <li>Equity Market Indices</li>
        </ul>

        <p>Data are sourced from national statistical agencies, multilateral banks, multilateral government bodies, and other publicly available sources.</p>

        <p>We then shortlist the top functions (hundreds) that correlate with prices to build our forecast algorithm. We re-test and re-build millions of forecast algorithms every month to account for changes in the global economy, markets and trade flows.</p>

        <p>Price forecasting presents a unique challenge that extends beyond traditional regression analysis. A number of approaches can be taken to apply artificial intelligence to price forecasting, including:</p>


        <ul>
            <li>Artificial Neural Networks (ANN)</li>
            <li>Adaptive Neuro Fuzzy Inference Systems (ANFIS)</li>
            <li>Autoregressive Integrated Moving Average (and its variants, including autoregressive fractional integrated moving average)</li>
        </ul>

        <p>Complete intelligence has undertaken detailed analysis and have prioritized the ARIMA-like approach over ANN, ANFIS and others. Through this process, we back test and reveal the forecast correlations and r-squared to give clients clarity on the actual levels of confidence for the forecast.</p>

